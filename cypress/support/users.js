// Function to create role-specific menu with removals
export const createRoleSpecificMenu = (role, menuType) => {
    let roleMenu;

    switch (menuType) {
        case "commonMenu":
            roleMenu = commonMenuItems.slice();
            break;
        case 'topMenu':
            roleMenu = commonTopMenuItems.slice();
            break;
        case 'leftMenu':
            roleMenu = commonLeftMenuItems.slice();
            break;
        case 'bottomMenu':
            roleMenu = commonBottomMenuItems.slice();
            break;
        default:
            roleMenu = [];
    }

    if (roleSpecificRemovals[role]) {
        const removals = roleSpecificRemovals[role][menuType] || [];
        // Check if the menu items have a "menu" property
        if (roleMenu.length > 0 && roleMenu[0].menu !== undefined) {
            roleMenu = roleMenu.filter((item) => !removals.includes(item.menu));
        } else {
            // If there's no "menu" property, assume it's a simple array of strings
            roleMenu = roleMenu.filter((item) => !removals.includes(item));
        }
    }

    return roleMenu;
};

